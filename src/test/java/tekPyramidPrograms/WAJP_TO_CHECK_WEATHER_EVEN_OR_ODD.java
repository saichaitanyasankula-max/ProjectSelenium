package tekPyramidPrograms;

public class WAJP_TO_CHECK_WEATHER_EVEN_OR_ODD {

	public static void main(String[] args) {
		
		int n=4;
		
		for (int i = 0; i <=n; i++) {
			
			if(i%2==0)
			{
				System.out.println(i+"----->"+"it is an even number");
			}
			else
			{
				System.out.println(i+"----->"+"it is an odd number");
			}
		}
	}
}
